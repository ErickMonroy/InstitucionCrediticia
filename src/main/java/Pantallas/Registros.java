private void solicitarPrestamo() {
    // Obtener los datos de préstamo desde la interfaz
    String nomPrestamista = txtNombres.getText();
    String aPaternoPtmista = txtAPaterno.getText();
    String aMaternoPtmista = txtAMaterno.getText();
    TipoPrestamo tipoPrestamo = TipoPrestamo.A; // Debes obtener esto desde la interfaz
    double tasaInteres = 0.05; // Debes obtener esto desde la interfaz
    double montoPrestamo = 1000.0; // Debes obtener esto desde la interfaz

    // Llamar al método de Prestamos para solicitar el préstamo
    Prestamos.solicitarPrestamo(nomPrestamista, aPaternoPtmista, aMaternoPtmista,
                              tipoPrestamo, tasaInteres,
                              montoPrestamo);
}

