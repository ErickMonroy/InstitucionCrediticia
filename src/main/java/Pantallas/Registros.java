    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

    System.out.println("Botón Guardar clicado");
    
    String nombre = txtNombres.getText();
    String aPaterno = txtAPaterno.getText();
    String aMaterno = txtAMaterno.getText();
    String edad = txtEdad.getText();
    String sexo = txtSexo.getText();
    String domicilio = txtDomicilio.getText();
    String numTelef = txtNumTelef.getText();
    String correo = txtCorreo.getText();

    System.out.println("Datos a guardar:");
    System.out.println("Nombre: " + nombre);
    System.out.println("Apellido Paterno: " + aPaterno);
    System.out.println("Apellido Materno: " + aMaterno);
    System.out.println("Edad: " + edad);
    System.out.println("Sexo: " + sexo);
    System.out.println("Domicilio: " + domicilio);
    System.out.println("Número de teléfono: " + numTelef);
    System.out.println("Correo Electronico: " + correo);

    int edadInt = Integer.parseInt(edad);
    Botones.guardar(nombre, aPaterno, aMaterno, edadInt, sexo, domicilio, numTelef, correo);

    txtNombres.setText("");
    txtAPaterno.setText("");
    txtAMaterno.setText("");
    txtEdad.setText("");
    txtSexo.setText("");
    txtDomicilio.setText("");
    txtNumTelef.setText("");
    txtCorreo.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed
    
